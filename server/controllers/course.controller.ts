// get course content only valid user

export const getCourseContent = CatchAsyncError(
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const userCourseList = req.user?.courses;

      const cousreId = req.params.id;

      const isCourseExists = userCourseList?.find(
        (course: any) => course.id === cousreId
      );

      if (!isCourseExists) {
        return next(new ErrorHandler("course not found", 400));
      }

      const course = await CourseModel.findById(cousreId);

      const content = course?.courseData;

      res.status(200).json({
        success: true,
        content,
      });
    } catch (error: any) {
      return next(new ErrorHandler(error.message, 500));
    }
  }
);
